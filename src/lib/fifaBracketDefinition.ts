/**
 * Array de los 12 grupos en orden alfabético (A → L). Usado por
 * `buildBestThirdsKey` y otras funciones de mapeo.
 */
export const ALL_GROUP_LETTERS: readonly GroupLetter[] = [
	"A",
	"B",
	"C",
	"D",
	"E",
	"F",
	"G",
	"H",
	"I",
	"J",
	"K",
	"L",
] as const;

/**
 * Genera la key de 12 chars a partir de los grupos cuyos terceros clasifican.
 *
 * El orden es siempre A→L, con `'1'` = clasifica, `'0'` = eliminado.
 *
 * @param qualifiedGroups - Array (o set) de grupos cuyos 3° están en el top 8.
 * @returns String de 12 chars tipo "111111110000".
 *
 * @example
 * buildBestThirdsKey(["A","B","C","D","E","F","G","H"])
 * // → "111111110000" (clasifican A-H, eliminados I-L)
 *
 * buildBestThirdsKey(["E","F","G","H","I","J","K","L"])
 * // → "000011111111" (clasifican E-L, eliminados A-D)
 *
 * buildBestThirdsKey([])
 * // → "000000000000" (ninguno clasifica — caso degenerado, no usado en práctica)
 */
export function buildBestThirdsKey(
	qualifiedGroups: readonly GroupLetter[],
): string {
	const set = new Set(qualifiedGroups);
	return ALL_GROUP_LETTERS.map((g) => (set.has(g) ? "1" : "0")).join("");
}

/**
 * Helper inverso: dada una key de 12 chars, retorna los grupos cuyos terceros
 * clasifican. Útil para debugging y tests.
 *
 * @param key - String de 12 chars (ej. "111111110000")
 * @returns Array de GroupLetter en orden alfabético.
 */
export function decodeBestThirdsKey(key: string): GroupLetter[] {
	if (key.length !== 12) {
		throw new Error(
			`decodeBestThirdsKey: key debe tener 12 chars, recibió "${key}" (${key.length})`,
		);
	}
	const result: GroupLetter[] = [];
	for (let i = 0; i < 12; i++) {
		const ch = key[i];
		const group = ALL_GROUP_LETTERS[i];
		if (ch === "1" && group) {
			result.push(group);
		} else if (ch !== "0") {
			throw new Error(
				`decodeBestThirdsKey: char inválido "${ch}" en posición ${i} (debe ser "0" o "1")`,
			);
		}
	}
	return result;
}

/**
 * Resuelve la asignación de "Mejor 3°" para una combinación dada de grupos
 * que clasifican.
 *
 * Si la combinación no está en `BEST_THIRDS_COMBINATIONS`, lanza un error
 * descriptivo. Esto NO debería pasar en producción porque las 495
 * combinaciones C(12,8) cubren todos los casos posibles. Si pasa, indica
 * un bug en este archivo o una inconsistencia de datos.
 *
 * @param qualifiedGroups - Los 8 grupos cuyos terceros clasifican.
 * @returns BestThirdsAssignment: para cada M74/M77/M79/M80/M81/M82/M85/M87,
 *          el grupo del 3° rival.
 *
 * @example
 * // Si clasifican A,B,C,D,E,F,G,H (key="111111110000"):
 * resolveBestThirdsAssignment(["A","B","C","D","E","F","G","H"])
 * // → { M74: "C", M77: "F", M79: "H", M80: "E", M81: "B", M82: "A", M85: "G", M87: "D" }
 */
export function resolveBestThirdsAssignment(
	qualifiedGroups: readonly GroupLetter[],
): BestThirdsAssignment {
	if (qualifiedGroups.length !== 8) {
		throw new Error(
			`resolveBestThirdsAssignment: esperaba 8 grupos, recibió ${qualifiedGroups.length} (${qualifiedGroups.join(", ")})`,
		);
	}
	const key = buildBestThirdsKey(qualifiedGroups);
	const assignment = BEST_THIRDS_COMBINATIONS[key];
	if (!assignment) {
		throw new Error(
			`Combinación de mejores terceros no encontrada: key="${key}". ` +
				`Grupos clasificados: ${qualifiedGroups.join(", ")}. ` +
				`BEST_THIRDS_COMBINATIONS debería contener las 495 combinaciones C(12,8).`,
		);
	}
	return assignment;
}

/**
 * Dado un grupo "anfitrión" (1° de grupo) y un array de 8 grupos clasificados
 * como mejores terceros, retorna el grupo del 3° que enfrentará a ese 1° en
 * el partido R32 correspondiente. Helper de alto nivel usado por el bracket
 * engine.
 *
 * @param firstPlaceGroup - Grupo del 1° (ej. "E" para M74)
 * @param qualifiedThirds - Los 8 grupos cuyos terceros clasifican
 * @returns El grupo del 3° rival (uno de los 8)
 * @throws Si no se puede encontrar el partido o la combinación
 *
 * @example
 * // ¿Quién enfrenta a 1°E si clasifican A,B,C,D,E,F,G,H?
 * getThirdForFirstPlace("E", ["A","B","C","D","E","F","G","H"])
 * // → "C" (M74: 1°E vs 3°C)
 */
export function getThirdForFirstPlace(
	firstPlaceGroup: GroupLetter,
	qualifiedThirds: readonly GroupLetter[],
): GroupLetter {
	// Mapeo: grupo del 1° → match FIFA. Solo 8 grupos tienen un partido
	// "1° vs Mejor 3°" en R32. Los otros 4 (C, F, H, J) juegan contra
	// un 2° de grupo, no contra un 3°.
	const firstToMatch: Partial<Record<GroupLetter, keyof BestThirdsAssignment>> =
		{
			E: "M74",
			I: "M77",
			A: "M79",
			L: "M80",
			D: "M81",
			G: "M82",
			B: "M85",
			K: "M87",
		};

	const match = firstToMatch[firstPlaceGroup];
	if (!match) {
		throw new Error(
			`getThirdForFirstPlace: el grupo ${firstPlaceGroup} no es un 1° que enfrenta a un mejor 3° en R32`,
		);
	}

	const assignment = resolveBestThirdsAssignment(qualifiedThirds);
	return assignment[match];
}

/**
 * Helper de búsqueda: dado un bracketId, retorna el `FIFAR32MatchDef` correspondiente.
 *
 * @param bracketId - ej. "R32-1", "R32-16"
 * @returns El match def, o `undefined` si no existe
 */
export function getR32MatchByBracketId(
	bracketId: string,
): FIFAR32MatchDef | undefined {
	return FIFA_R32_MATCHUPS.find((m) => m.bracketId === bracketId);
}

/**
 * Helper de búsqueda: dado un número FIFA, retorna el `FIFAR32MatchDef` correspondiente.
 *
 * @param fifaNumber - ej. 73, 88
 * @returns El match def, o `undefined` si no existe
 */
export function getR32MatchByFifaNumber(
	fifaNumber: number,
): FIFAR32MatchDef | undefined {
	return FIFA_R32_MATCHUPS.find((m) => m.fifaNumber === fifaNumber);
}

