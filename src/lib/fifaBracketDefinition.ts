/**
 * Letra de grupo (A-L). El Mundial 2026 tiene 12 grupos de 4 equipos cada uno.
 */
export type GroupLetter =
	| "A"
	| "B"
	| "C"
	| "D"
	| "E"
	| "F"
	| "G"
	| "H"
	| "I"
	| "J"
	| "K"
	| "L";

/**
 * Tipo de slot en un partido R32:
 * - "1st"     = 1° del grupo
 * - "2nd"     = 2° del grupo
 * - "best3rd" = Mejor 3° (rank 1-8, seleccionado vía `BEST_THIRDS_COMBINATIONS`)
 */
export type FIFASlotType = "1st" | "2nd" | "best3rd";

/**
 * Definición de un slot en un partido R32.
 *
 * Si `slotType === "best3rd"`, `bestThirdPool` indica los grupos que pueden
 * aportar el 3° rival (siempre excluye al grupo del 1° rival). El grupo
 * concreto se resuelve en runtime vía `BEST_THIRDS_COMBINATIONS`.
 */
export interface FIFASlotDef {
	/** Tipo de slot (cómo se llena) */
	slotType: FIFASlotType;
	/** Letra del grupo de donde sale el equipo (null solo si best3rd sin pool) */
	group: GroupLetter;
	/**
	 * Si `slotType === "best3rd"`, pool de grupos que pueden aportar el 3°
	 * rival. Para otros slotTypes, es `undefined`.
	 */
	bestThirdPool?: readonly GroupLetter[];
}

/**
 * Definición de un partido de 16vos de final (M73-M88).
 *
 * Cada partido tiene dos slots. Los partidos "1° vs Mejor 3°" tienen el
 * `bestThirdPool` poblado en su slot B (el del 3°); los partidos "2° vs 2°"
 * y "1° vs 2°" no usan `bestThirdPool`.
 */
export interface FIFAR32MatchDef {
	/** Número oficial FIFA del partido (73-88) */
	fifaNumber: number;
	/** ID de bracket interno: "R32-1" a "R32-16" (orden FIFA) */
	bracketId: string;
	slotA: FIFASlotDef;
	slotB: FIFASlotDef;
}

/**
 * Definición de un partido de 8vos de final (M89-M96).
 * Cada partido cruza los ganadores de 2 partidos de R32.
 */
export interface FIFAR16MatchDef {
	/** Número oficial FIFA del partido (89-96) */
	fifaNumber: number;
	/** ID de bracket interno: "R16-1" a "R16-8" */
	bracketId: string;
	/** BracketId del partido R32 cuyo ganador llena slot A (ej. "R32-1") */
	sourceMatchA: string;
	/** BracketId del partido R32 cuyo ganador llena slot B (ej. "R32-3") */
	sourceMatchB: string;
}

/** Definición de un partido de cuartos de final (M97-M100). */
export interface FIFAQFMatchDef {
	/** Número oficial FIFA (97-100) */
	fifaNumber: number;
	/** ID de bracket interno: "QF-1" a "QF-4" */
	bracketId: string;
	sourceMatchA: string;
	sourceMatchB: string;
}

/** Definición de un partido de semifinal (M101-M102). */
export interface FIFASFMatchDef {
	/** Número oficial FIFA (101-102) */
	fifaNumber: number;
	/** ID de bracket interno: "SF-1" a "SF-2" */
	bracketId: string;
	sourceMatchA: string;
	sourceMatchB: string;
}

/** Definición de la final (M104). */
export interface FIFAFinalMatchDef {
	/** Número oficial FIFA: 104 */
	fifaNumber: 104;
	/** ID de bracket interno: "F-1" */
	bracketId: "F-1";
	sourceMatchA: string;
	sourceMatchB: string;
}

/** Definición del partido por el 3er puesto (M103). */
export interface FIFAThirdPlaceMatchDef {
	/** Número oficial FIFA: 103 */
	fifaNumber: 103;
	/** ID de bracket interno: "3RD-1" */
	bracketId: "3RD-1";
	sourceMatchA: string;
	sourceMatchB: string;
}

/**
 * Asignación de "Mejor 3°" para los 8 partidos 1° vs Best 3°.
 *
 * Cada partido M74, M77, M79, M80, M81, M82, M85, M87 tiene asignado el grupo
 * del que sale el 3° rival del 1°. Los grupos son los 8 que clasifican como
 * mejores terceros en esa combinación específica.
 */
export interface BestThirdsAssignment {
	/** M74: 1°E vs Best 3° de {A,B,C,D,F} */
	M74: GroupLetter;
	/** M77: 1°I vs Best 3° de {C,D,F,G,H} */
	M77: GroupLetter;
	/** M79: 1°A vs Best 3° de {C,E,F,H,I} */
	M79: GroupLetter;
	/** M80: 1°L vs Best 3° de {E,H,I,J,K} */
	M80: GroupLetter;
	/** M81: 1°D vs Best 3° de {B,E,F,I,J} */
	M81: GroupLetter;
	/** M82: 1°G vs Best 3° de {A,E,H,I,J} */
	M82: GroupLetter;
	/** M85: 1°B vs Best 3° de {E,F,G,I,J} */
	M85: GroupLetter;
	/** M87: 1°K vs Best 3° de {D,E,I,J,L} */
	M87: GroupLetter;
}

