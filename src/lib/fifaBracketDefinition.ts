/**
 * `fifaBracketDefinition` — Fuente única de verdad para el bracket del Mundial 2026.
 *
 * ============================================================================
 * PROPÓSITO
 * ============================================================================
 * Este módulo contiene la definición **estática y oficial** de los cruces del
 * Mundial 2026 según la numeración FIFA (M73-M104). El bracket engine actual
 * (`bracketEngine.ts`) implementa una versión simplificada con cruces
 * inventados (pares A-B, C-D, etc.); este archivo es la fuente que se usará
 * para refactorizar ese engine a la estructura oficial.
 *
 * ============================================================================
 * CONTENIDO
 * ============================================================================
 * 1. **16 cruces R32** (M73-M88) — Numeración oficial FIFA, con el slot A
 *    siendo el equipo "anfitrión" según las convenciones del fixture.
 * 2. **8 cruces R16** (M89-M96) — Winners de R32 se cruzan según el árbol
 *    oficial.
 * 3. **4 cruces QF** (M97-M100) — Winners de R16.
 * 4. **2 cruces SF** (M101-M102) — Winners de QF.
 * 5. **Final (M104)** y **3RD (M103)** — Winners/perdedores de SF.
 * 6. **495 combinaciones de mejores terceros** — Tabla completa publicada en
 *    el Anexo C del reglamento oficial FIFA. Cada combinación indica qué
 *    equipo (3° de qué grupo) enfrenta a cada 1° en los 8 partidos
 *    "1° vs Mejor 3°".
 *
 * ============================================================================
 * NUMERACIÓN FIFA
 * ============================================================================
 * La numeración FIFA M73-M104 cubre las 5 rondas eliminatorias + 3er puesto:
 * - M73-M88: 16vos de final (Round of 32)
 * - M89-M96: 8vos de final  (Round of 16)
 * - M97-M100: 4tos de final (Quarterfinals)
 * - M101-M102: Semifinales
 * - M103: Partido por el 3er puesto
 * - M104: Final
 *
 * ============================================================================
 * POOLS DE "MEJOR 3°"
 * ============================================================================
 * Los 8 partidos "1° vs Mejor 3°" tienen un pool fijo de grupos que pueden
 * aportar el 3° rival. Por ejemplo, M74 (1°E vs Best 3°) solo puede enfrentar
 * al 3° de uno de los grupos A, B, C, D, F (nunca E, porque E ya tiene al 1°).
 *
 * La tabla de 495 combinaciones codifica la asignación concreta para cada
 * escenario de "qué 8 grupos clasifican". Cada combinación es un mapa M73-M88
 * → grupo del 3° rival (vacío para M73, M75, M76, M78, M83, M84, M86, M88
 * que no son 1° vs 3°).
 *
 * ============================================================================
 * FUENTES
 * ============================================================================
 * - Reglamento oficial FIFA 2026, Anexo C (495 combinaciones).
 * - Wikipedia, "2026 FIFA World Cup knockout stage" (consultada el 2026-06-25):
 *   https://en.wikipedia.org/wiki/2026_FIFA_World_Cup_knockout_stage
 *
 * La tabla de Wikipedia fue extraída programáticamente (ver
 * `src/__tests__/fifaBracketDefinition.test.ts` para tests de validación).
 *
 * @module lib/fifaBracketDefinition
 */

